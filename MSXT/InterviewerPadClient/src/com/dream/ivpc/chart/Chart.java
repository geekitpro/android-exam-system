package com.dream.ivpc.chart;

public class Chart {
	private String bgColor;

	public Chart() {
		super();
	}

	public Chart(String bgColor) {
		super();
		this.bgColor = bgColor;
	}

	public String getBgColor() {
		return bgColor;
	}

	public void setBgColor(String bgColor) {
		this.bgColor = bgColor;
	}
}
